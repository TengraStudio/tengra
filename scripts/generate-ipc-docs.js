#!/usr/bin/env node
/**
 * Auto-generates IPC API reference from handler files in src/main/ipc/.
 * Usage: node scripts/generate-ipc-docs.js
 */
const fs = require('fs');
const path = require('path');

const IPC_DIR = path.join(__dirname, '..', 'src', 'main', 'ipc');
const OUT_FILE = path.join(__dirname, '..', 'docs', 'api', 'IPC_REFERENCE.md');
const CHANNEL_RE = /ipcMain\.handle\(\s*'([^']+)'/g;
const JSDOC_RE = /\/\*\*\s*([\s\S]*?)\*\/\s*\n\s*ipcMain\.handle\(\s*'([^']+)'/g;

function extractHandlers(filePath) {
  const src = fs.readFileSync(filePath, 'utf-8');
  const docs = new Map();

  // Extract JSDoc-annotated handlers
  let m;
  while ((m = JSDOC_RE.exec(src)) !== null) {
    const comment = m[1].replace(/^\s*\*\s?/gm, '').trim();
    docs.set(m[2], comment);
  }

  // Extract all channel names
  const channels = [];
  while ((m = CHANNEL_RE.exec(src)) !== null) {
    channels.push({ channel: m[1], description: docs.get(m[1]) || '' });
  }
  return channels;
}

function domainLabel(fileName) {
  return fileName.replace(/\.ts$/, '').split('-').map(w => w[0].toUpperCase() + w.slice(1)).join(' ');
}

function generate() {
  const files = fs.readdirSync(IPC_DIR).filter(f => f.endsWith('.ts') && f !== 'index.ts');
  const groups = [];

  for (const file of files.sort()) {
    const handlers = extractHandlers(path.join(IPC_DIR, file));
    if (handlers.length > 0) {
      groups.push({ file, label: domainLabel(file), handlers });
    }
  }

  const totalCount = groups.reduce((s, g) => s + g.handlers.length, 0);
  const lines = [
    '# IPC API Reference',
    '',
    `> Auto-generated by \`scripts/generate-ipc-docs.js\` — **${totalCount} handlers** across **${groups.length} modules**.`,
    '',
    '## Table of Contents',
    '',
    ...groups.map(g => `- [${g.label}](#${g.label.toLowerCase().replace(/ /g, '-')}) (${g.handlers.length})`),
    '',
    '---',
    '',
  ];

  for (const g of groups) {
    lines.push(`## ${g.label}`, '', `_Source: \`src/main/ipc/${g.file}\`_`, '');
    lines.push('| Channel | Description |', '|---------|-------------|');
    for (const h of g.handlers) {
      const desc = h.description.replace(/\n/g, ' ').replace(/\|/g, '\\|');
      lines.push(`| \`${h.channel}\` | ${desc || '—'} |`);
    }
    lines.push('', '---', '');
  }

  fs.mkdirSync(path.dirname(OUT_FILE), { recursive: true });
  fs.writeFileSync(OUT_FILE, lines.join('\n'), 'utf-8');
  console.log(`Generated ${OUT_FILE} (${totalCount} handlers, ${groups.length} modules)`);
}

generate();
